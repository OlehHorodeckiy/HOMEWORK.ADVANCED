const getSubjects = (subject) => {
    let subjects = [];
    for (let key in subject) {
      const subjectn = (
        key.substring(0, 1).toUpperCase() + key.substring(1).toLowerCase()
      ).replace("_", " ");
      subjects.push(subjectn);
    }
    return subjects;
  };
  
